#include <iostream>

using namespace std;

/****************************************************************************
 *  Function Name : main
 ****************************************************************************/
int main () {
	float a, b;
	cin >> a >> b;
	try {
		if (b == 0) {
			throw b;
		}
		cout  << a / b << endl;
	} catch( float n) {
		  cout << "错误原因是b = " << n << endl;
	}
	return 0;
}

